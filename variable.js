// Variable

// Five things to declare a variable
/**
 * 1> key word: var / let/ const
 *
 * 2> variable Name
 *
 * 3> Equal Sign: (=)
 *
 * 4> Variable value
 *
 * 5> Semi-colon: (:)
 */

// Example:
var money = 25;

// keyword: var, variable-name: money, Equal-sign: (=), Variable-value: 25, Semi-color: (;)

let age = 22;

// keyword: let, Variable Name; age, Value: 22

console.log(money);
console.log(age);
